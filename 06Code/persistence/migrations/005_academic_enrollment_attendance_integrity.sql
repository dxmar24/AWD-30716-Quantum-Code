CREATE TABLE IF NOT EXISTS class_group_enrollments (
  id UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
  student_id UUID NOT NULL REFERENCES students(id),
  class_group_id UUID NOT NULL REFERENCES class_groups(id),
  status VARCHAR(20) NOT NULL DEFAULT 'active',
  starts_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  ends_at TIMESTAMPTZ,
  enrolled_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  withdrawal_reason TEXT,
  created_by UUID REFERENCES users(id),
  created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  CONSTRAINT class_group_enrollments_student_group_unique UNIQUE(student_id, class_group_id),
  CONSTRAINT class_group_enrollments_status_check CHECK (
    status IN ('active','waitlisted','trial','frozen','withdrawn','completed')
  ),
  CONSTRAINT class_group_enrollments_dates_check CHECK (ends_at IS NULL OR ends_at >= starts_at)
);

CREATE INDEX IF NOT EXISTS idx_class_group_enrollments_group_status
  ON class_group_enrollments(class_group_id, status);

CREATE INDEX IF NOT EXISTS idx_class_group_enrollments_student_status
