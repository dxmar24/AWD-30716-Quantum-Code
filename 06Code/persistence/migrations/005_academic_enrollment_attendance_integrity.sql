WITH ranked AS (
  SELECT
    id,
    ROW_NUMBER() OVER (
      PARTITION BY attendance_record_id
      ORDER BY CASE WHEN status = 'approved' THEN 0 ELSE 1 END, reviewed_at DESC NULLS LAST, id
    ) AS row_number
  FROM absence_justifications
  WHERE status IN ('pending','approved')
)
UPDATE absence_justifications justification
SET
  status = 'rejected',
  review_notes = CONCAT_WS(' ', justification.review_notes, 'Rejected by migration 005 because another active justification exists.'),
  reviewed_at = COALESCE(justification.reviewed_at, now())
FROM ranked
WHERE ranked.id = justification.id
  AND ranked.row_number > 1;

CREATE UNIQUE INDEX IF NOT EXISTS idx_absence_justifications_one_active
  ON absence_justifications(attendance_record_id)
  WHERE status IN ('pending','approved');

