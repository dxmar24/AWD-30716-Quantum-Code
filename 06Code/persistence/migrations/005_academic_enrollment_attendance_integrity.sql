SELECT
  attendance.id,
  'Legacy justified attendance migrated to an approved absence justification.',
  'approved',
  'Generated by migration 005 to preserve the historical excuse decision.',
  now()
FROM student_attendance_records attendance
WHERE attendance.status = 'justified'
  AND NOT EXISTS (
    SELECT 1
    FROM absence_justifications justification
    WHERE justification.attendance_record_id = attendance.id
      AND justification.status = 'approved'
  );

